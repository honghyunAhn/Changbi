CREATE TABLE BOGUN.T_NOTE
(
  ID                NUMBER(12)                  NOT NULL,
  FROM_USER_ID      VARCHAR2(100 BYTE)          NOT NULL,
  TO_USER_ID        VARCHAR2(100 BYTE)          NOT NULL,
  TITLE             VARCHAR2(100 BYTE)          NOT NULL,
  NOTE              VARCHAR2(4000 BYTE),
  SAVE_YN           CHAR(1 BYTE)                DEFAULT 'Y',
  PROC_YN           CHAR(1 BYTE)                DEFAULT 'N',
  FROM_USER_USE_YN  CHAR(1 BYTE)                DEFAULT 'Y'                   NOT NULL,
  TO_USER_USE_YN    CHAR(1 BYTE)                DEFAULT 'Y'                   NOT NULL,
  SEND_DATE         VARCHAR2(14 BYTE)           DEFAULT NULL
)
