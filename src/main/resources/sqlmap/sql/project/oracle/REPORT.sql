COMMENT ON COLUMN BOGUN.T_REPORT.FILE_ID IS '문제 답안 파일ID(과제의 정답을 파일로 제출한 경우 저장)';

COMMENT ON COLUMN BOGUN.T_REPORT.USER_FILE_ID IS '출석고사 증명사진';

COMMENT ON COLUMN BOGUN.T_REPORT.MARK_YN IS '채점여부(Y/N)';

COMMENT ON COLUMN BOGUN.T_REPORT.ORDER_NUM IS '순서';

COMMENT ON COLUMN BOGUN.T_REPORT.USE_YN IS '사용여부(Y : 사용, N : 미사용)';

COMMENT ON COLUMN BOGUN.T_REPORT.REG_USER IS '등록자 ID';

