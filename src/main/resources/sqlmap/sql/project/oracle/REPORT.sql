COMMENT ON COLUMN BOGUN.T_REPORT.QUIZ_TYPE IS '시험 종류(1:출석고사, 2:온라인평가, 3:온라인과제)';

COMMENT ON COLUMN BOGUN.T_REPORT.EXAM_SPOT_ID IS '출석평가 시 고사장ID(3자리로 고정)';

COMMENT ON COLUMN BOGUN.T_REPORT.EXAM_NUM IS '출석평가 수험번호(고사장코드(3)+교시(1)+일련번호(4) : 총 8자리)';

COMMENT ON COLUMN BOGUN.T_REPORT.ANSWER IS '출석평가문제답안(답안OX : OOOXOOOOXOXXXXOOOOOOXXXOXOO 또는 1O2X3O4O5X6X7X8O)';

