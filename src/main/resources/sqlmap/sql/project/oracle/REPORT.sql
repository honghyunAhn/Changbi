CREATE TABLE BOGUN.T_REPORT
(
  ID            NUMBER(12)                      NOT NULL,
  LEARN_APP_ID  NUMBER(12)                      NOT NULL,
  CARDINAL_ID   NUMBER(12) 		                NOT NULL,
  COURSE_ID     VARCHAR2(12 BYTE)               NOT NULL,
  QUIZ_ID       VARCHAR2(20 BYTE)               NOT NULL,
  USER_ID       VARCHAR2(20 BYTE)               NOT NULL,
  QUIZ_TYPE     CHAR(1 BYTE)                    DEFAULT '2',
  EXAM_SPOT_ID  VARCHAR2(3 BYTE)                DEFAULT NULL,
  EXAM_NUM      VARCHAR2(8 BYTE)                DEFAULT NULL,
  ANSWER        VARCHAR2(200 BYTE)              DEFAULT NULL,
  SCORE         NUMBER(3)                       DEFAULT '0',
  FILE_ID       VARCHAR2(20 BYTE)               DEFAULT NULL,
  USER_FILE_ID  VARCHAR2(20 BYTE)               DEFAULT NULL,
  MARK_YN       CHAR(1 BYTE)                    DEFAULT 'N',
  ORDER_NUM     NUMBER(3)                       DEFAULT '1',
  USE_YN        CHAR(1 BYTE)                    DEFAULT 'Y',
  REG_USER      VARCHAR2(100 BYTE)              DEFAULT NULL,
  REG_DATE      VARCHAR2(14 BYTE)               DEFAULT NULL,
  UPD_USER      VARCHAR2(100 BYTE)              DEFAULT NULL,
  UPD_DATE      VARCHAR2(14 BYTE)               DEFAULT NULL
)
TABLESPACE BOGUN
PCTUSED    40
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
MONITORING;

